//! Cross-implementation interop fixtures for the encrypted-CEK envelope.
//!
//! `MS_JDBC_ENVELOPE` below was produced by Microsoft's shipped JDBC driver
//! (mssql-jdbc 12.10.1, `SQLServerColumnEncryptionJavaKeyStoreProvider`,
//! public API) wrapping the test CEK with the RSA key in
//! `fixtures/test_cmk_rsa2048.pem`. The same canonical layout is used by all
//! Microsoft Always Encrypted clients (.NET, SSMS, ODBC, PowerShell):
//!
//! ```text
//! version(0x01) || keyPathLen(u16 LE) || cipherLen(u16 LE)
//!               || keyPath(UTF-16LE)  || ciphertext || RSA-PKCS1-SHA256 signature
//! ```
//!
//! Decrypting it proves the parser layout AND the mandatory signature
//! verification match what real-world provisioning tools emit. A self
//! round-trip cannot catch a layout that is internally consistent but
//! non-conformant (the v0.9.0 lesson, twice over).
//!
//! Regenerate with `fixtures/CekHarness.java` (full recipe in its header);
//! the fixture is stable because parse + RSA-OAEP decrypt of a captured
//! envelope is deterministic.

