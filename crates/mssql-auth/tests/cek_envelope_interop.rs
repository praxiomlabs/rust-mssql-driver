/// Build an envelope ourselves (OAEP-SHA1 wrap, canonical layout,
/// PKCS1-SHA256 signature) and round-trip it through our own provider.
///
/// Run with `--nocapture` to emit the envelope hex; feeding it to
/// `fixtures/CekHarness.java` (recipe in its header) proves Microsoft's
/// binary decrypts it and accepts our signature (done for the PR; OAEP is
/// randomized so the bytes differ per run, which is why this isn't a fixed
/// fixture).
#[tokio::test]
async fn builds_envelope_other_clients_accept() {
    use rsa::{Oaep, Pkcs1v15Sign, RsaPrivateKey, pkcs8::DecodePrivateKey};
    use sha1::Sha1;
    use sha2::{Digest, Sha256};

    let key = RsaPrivateKey::from_pkcs8_pem(TEST_CMK_PEM).expect("fixture key parses");
    let cek = expected_cek();

    let mut rng = rand::thread_rng();
    let ciphertext = key
        .to_public_key()
        .encrypt(&mut rng, Oaep::new::<Sha1>(), &cek)
        .expect("RSA-OAEP wrap");

    let mut envelope = mssql_auth::cek_envelope::build_signed_portion(KEY_PATH, &ciphertext);
    let digest: [u8; 32] = Sha256::digest(&envelope).into();
    let signature = key
        .sign(Pkcs1v15Sign::new::<Sha256>(), &digest)
        .expect("CMK signs");
    envelope.extend_from_slice(&signature);

    let hex: String = envelope.iter().map(|b| format!("{b:02X}")).collect();
    println!("OUR_ENVELOPE:{hex}");

    let mut store = InMemoryKeyStore::new();
    store
        .add_key(KEY_PATH, TEST_CMK_PEM)
        .expect("fixture key parses");
    let decrypted = store
        .decrypt_cek(KEY_PATH, "RSA_OAEP", &envelope)
        .await
        .expect("our own envelope round-trips");
    assert_eq!(decrypted, cek);
}

#[tokio::test]
async fn decrypts_microsoft_provisioned_cek() {
    let mut store = InMemoryKeyStore::new();
    store
        .add_key(KEY_PATH, TEST_CMK_PEM)
        .expect("fixture key parses");

    let cek = store
        .decrypt_cek(KEY_PATH, "RSA_OAEP", &MS_JDBC_ENVELOPE)
        .await
        .expect("Microsoft-provisioned CEK envelope must parse, verify, and decrypt");
    assert_eq!(cek, expected_cek());
}
